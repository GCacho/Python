#Programa que calcula si el número ingresado por el usuario es mayor menor o igual a 5
numero = int(input("Escribe un número: "))
if numero > 5:
    print("Es mayor a 5")
elif numero == 5:
    print("Es igual a 5")
else:
    print("Es menor a 5")

